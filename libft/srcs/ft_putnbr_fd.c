#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	char c;

	if (nbr == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (nbr < 0)
	{
		nbr = nbr * -1;
		ft_putchar_fd('-', fd);
	}
	if (nbr % 10 != nbr)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
	{
		c = nbr + '0';
		ft_putchar_fd(c, fd);
	}
}
