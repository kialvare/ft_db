#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned int i;

	i = (unsigned int)c;
	if ((i >= 'A' && i <= 'Z') ||
		(i >= 'a' && i <= 'z'))
		return (1);
	else
		return (0);
}
